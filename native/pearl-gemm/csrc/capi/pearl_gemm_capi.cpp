// --- noise_B & noisy_gemm: torch-free dispatch -----------------------------
//
// Mirrors the body of `noise_B` / `noisy_gemm` in csrc/gemm/pearl_gemm_api.cpp
// but builds PearlAPIParams directly from the C-ABI struct's raw void*
// pointers, fetches dprops via cudaGetDeviceProperties, takes the stream
// from the C-ABI argument (rather than at::cuda::getCurrentCUDAStream),
// and hard-codes the fp16 noising path (skip int32 split-K — pure-miner
// never uses it).

// Internal layout of a pearl-gemm workspace handle.  One contiguous device
// allocation, sliced into three sub-buffers (each 256-byte-aligned):
//
//   [ noiseA_scratch ][ noiseB_scratch ][ transcript ]
//
// The handle is opaque to callers (passed as void* through the C ABI).
namespace {

constexpr size_t kWorkspaceAlign = 256;

inline size_t align_up(size_t x, size_t a) { return (x + a - 1) & ~(a - 1); }

struct PearlCapiWorkspace {
  void*   base_ptr;
  size_t  total_bytes;
  size_t  off_noiseA;
  size_t  off_noiseB;
  size_t  off_transcript;
  size_t  na_bytes;
  size_t  nb_bytes;
  size_t  transcript_bytes;
  int32_t m, n, k, r;
  int     with_noise_A;
  int     with_noise_B;
  int     device_id = 0;

  // Cached per-σ constants — populated by pearl_capi_workspace_install_params.
  // Default-initialised to zero; params_installed gates pearl_capi_iter use.
  bool                      params_installed = false;
  PearlCapiWorkspaceParams  params{};
  PearlCapiNoisyGemmParams  ng_template{};  // pre-built, only header_ptr varies

  // Optional CUDA graph for the fixed-size IterBatch hot path.
  bool             graph_ready = false;
  int32_t          graph_batch_count = 0;
  cudaGraph_t      iter_graph = nullptr;
  cudaGraphExec_t  iter_graph_exec = nullptr;
  uint64_t*        graph_seed_lo_host = nullptr;  // pinned host slot
  uint64_t*        graph_seed_lo_dev = nullptr;   // device slot

  void* noiseA_ptr() const {
    return na_bytes ? static_cast<char*>(base_ptr) + off_noiseA : nullptr;
  }
  void* noiseB_ptr() const {
    return nb_bytes ? static_cast<char*>(base_ptr) + off_noiseB : nullptr;
  }
  void* transcript_ptr() const {
    return transcript_bytes ? static_cast<char*>(base_ptr) + off_transcript
                            : nullptr;
  }
};

void destroy_iter_graph(PearlCapiWorkspace* ws) {
  if (!ws) return;
  if (ws->iter_graph_exec) {
    cudaGraphExecDestroy(ws->iter_graph_exec);
    ws->iter_graph_exec = nullptr;
  }
  if (ws->iter_graph) {
    cudaGraphDestroy(ws->iter_graph);
    ws->iter_graph = nullptr;
  }
  if (ws->graph_seed_lo_dev) {
    cudaFree(ws->graph_seed_lo_dev);
    ws->graph_seed_lo_dev = nullptr;
  }
  if (ws->graph_seed_lo_host) {
    cudaFreeHost(ws->graph_seed_lo_host);
    ws->graph_seed_lo_host = nullptr;
  }
  ws->graph_ready = false;
  ws->graph_batch_count = 0;
}

}  // namespace

